    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        Client passenger = new Client(user.getText(), email.getText().toLowerCase(), phone.getText(), address.getText(), ssn.getText(), pass.getText(), rePassword.getText(), admin.getText().toLowerCase());

        java.util.List<String> errors = passenger.validation();
        if (errors.size() > 0){
            JOptionPane.showMessageDialog(null, errors.toArray());
            return;
        }
        else if (errors.size() == 0){
            if (addCustomer(passenger) == 1)
            JOptionPane.showMessageDialog(null, "Registration Completed");
            else
            JOptionPane.showMessageDialog(null, "Registration Failed");
        }
    }//GEN-LAST:event_submitActionPerformed

