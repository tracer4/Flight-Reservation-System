    public static int addCustomer(Client obj){
        String user = "";
        
        if (obj.getAdmin().equals("15@admin")){
            try {
                PreparedStatement stmt = connection.prepareStatement("insert into Admin (ID, Name, SSN , Address, Phone, Email, Password) values (?, ?, ?, ?, ?, ?, ?)");
                stmt.setInt(1, maxAdminID());
                stmt.setString(2, obj.getName());
                stmt.setString(3, obj.getSsn());
                stmt.setString(4, obj.getAddress());
                stmt.setString(5, obj.getPhone());
                stmt.setString(6, obj.getEmail());
                stmt.setString(7, obj.getPass());
                stmt.executeUpdate();
                return 1;
            } catch (SQLException e) {
                e.printStackTrace();
                return -1;
            }
        }
        else if (obj.getAdmin().isEmpty() || obj.getAdmin().equals("no")){
            try {
                PreparedStatement stmt = connection.prepareStatement("insert into Customer (ID, Name, SSN , Address, Phone, Email, Password) values (?, ?, ?, ?, ?, ?, ?);");
                stmt.setInt(1, maxCustomerID());
                stmt.setString(2, obj.getName());
                stmt.setString(3, obj.getSsn());
                stmt.setString(4, obj.getAddress());
                stmt.setString(5, obj.getPhone());
                stmt.setString(6, obj.getEmail());
                stmt.setString(7, obj.getPass());
                stmt.executeUpdate();
                return 1;
            } catch (SQLException e) {
                e.printStackTrace();
                return -1;
            }
        }
        return 0;
    }

